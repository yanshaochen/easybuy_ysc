package cn.happy.bean;

public class Easybuy_product {
    private Long ep_id;
    private String ep_name;
    private String ep_description;
    private Double ep_price;
    private Long ep_stock;
    private Long ep_category_id;
    private String ep_img;

    public Long getEp_id() {
        return ep_id;
    }

    public void setEp_id(Long ep_id) {
        this.ep_id = ep_id;
    }

    public String getEp_name() {
        return ep_name;
    }

    public void setEp_name(String ep_name) {
        this.ep_name = ep_name;
    }

    public String getEp_description() {
        return ep_description;
    }

    public void setEp_description(String ep_description) {
        this.ep_description = ep_description;
    }

    public Double getEp_price() {
        return ep_price;
    }

    public void setEp_price(Double ep_price) {
        this.ep_price = ep_price;
    }

    public Long getEp_stock() {
        return ep_stock;
    }

    public void setEp_stock(Long ep_stock) {
        this.ep_stock = ep_stock;
    }

    public Long getEp_category_id() {
        return ep_category_id;
    }

    public void setEp_category_id(Long ep_category_id) {
        this.ep_category_id = ep_category_id;
    }

    public String getEp_img() {
        return ep_img;
    }

    public void setEp_img(String ep_img) {
        this.ep_img = ep_img;
    }
}
